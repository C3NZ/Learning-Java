package Basic.Lesson14;



/**
 * Lesson on a conditional operator
 * @author https://github.com/C3NZ
 *
 */
public class ConditionalOperator{

	public static void main(String[] args) {
		
		int num = 21;
		
		//asks, is variable "num" greater than 20? and then does one of two operations depending on if its true or false.
		//separated by a semicolon
		System.out.println(num > 20 ? "num greater than 20" : "num less than 20");

	}

}
